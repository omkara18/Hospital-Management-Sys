    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
       String patientID=patientIDField.getText();
       String name = nameField.getText();
       String contactNo = contactNoField.getText();
       String age = ageField.getText();
       String gender = genderField.getText();
       String bloodGroup =bloodGroupField.getText();
       String address = addressField.getText();
      String anyMajorDiseases = anyMajorField.getText();
      try
      {
             Connection con =ConnectionProvider.getCon();
             Statement st =con.createStatement();
             st.executeUpdate("update patient set name='"+name+"',contactNo='"+contactNo+"',age='"+age+"',gender='"+gender+"',bloodGroup='"+bloodGroup+"',address='"+address+"',anyMajorDiseases='"+anyMajorDiseases+"' where patientID='"+patientID+"'" );
             JOptionPane.showMessageDialog(null,"Updated....."); 
             setVisible(false);
             new updatePatientRecord().setVisible(true);
      
      }
      catch(HeadlessException | SQLException e)
      {
          JOptionPane.showMessageDialog(null,"Please!! Enter Correct Data");     
 
      }
       

        
    }//GEN-LAST:event_updateButtonActionPerformed

