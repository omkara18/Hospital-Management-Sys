    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        String patientID=patientIDField.getText();
        try
        {
             Connection con = ConnectionProvider.getCon();
             Statement st = con.createStatement();
             ResultSet rs=st.executeQuery("Select *from patient where patientID ='"+patientID+"'");
             if(rs.next())
             {
                 nameField.setText(rs.getString(2));
                 contactNoField.setText(rs.getString(3));
                 ageField.setText(rs.getString(4));
                 genderField.setText(rs.getString(5));
                 bloodGroupField.setText(rs.getString(6));
                 addressField.setText(rs.getString(7));
                 anyMajorField.setText(rs.getString(8));  
                 patientIDField.setEditable(false);
             }
             else
             {
                                JOptionPane.showMessageDialog(null,"Patient ID Does Not Exist");     

             }    
        }
        catch(Exception e)
        {
               

        }
            
    }//GEN-LAST:event_searchButtonActionPerformed

